public class VariableConstants {
    public static void main(String[] args) {
        final int NUM1; // ค่าคงที่ = 10
        final int NUM2; // ค่าคงที่ = 20
        NUM1 = 100;
        NUM2 = 200;
        System.out.println("ค่าคงที่ 1 = " + NUM1);// แสดงผล num1
        System.out.println("ค่าคงที่ 2 = " + NUM2);// แสดงผล num2
    }
}
